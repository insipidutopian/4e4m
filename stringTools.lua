local stringTools = {}
 
print( "stringTools.lua has been loaded" )

function stringTools.split(inputstr, sep)
    if sep == nil then
        sep = "%s"
    end
    local t={}
    for str in string.gmatch(inputstr, "([^"..sep.."]+)") do
        table.insert(t, str)
    end
    return t
end

return stringTools
